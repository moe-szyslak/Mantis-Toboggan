  t.equal(geez('1111'), '፲፩፻፲፩', '፲፩፻፲፩');
  t.equal(geez('11111'), '፼፲፩፻፲፩', '፼፲፩፻፲፩');
  t.equal(geez('111111'), '፲፩፼፲፩፻፲፩', '፲፩፼፲፩፻፲፩');
  t.equal(geez('1111111'), '፻፲፩፼፲፩፻፲፩', '፻፲፩፼፲፩፻፲፩');
