  t.equal(geez('15161513'), '፲፭፻፲፮፼፲፭፻፲፫', '፲፭፻፲፮፼፲፭፻፲፫');
  t.equal(geez('10101011'), '፲፻፲፼፲፻፲፩', '፲፻፲፼፲፻፲፩');
  t.equal(geez('3'), '፫', '፫');
  t.equal(geez('30'), '፴', '፴');
  t.equal(geez('33'), '፴፫', '፴፫');
  t.equal(geez('303'), '፫፻፫', '፫፻፫');
  t.equal(geez('3003'), '፴፻፫', '፴፻፫');
  t.equal(geez('3030'), '፴፻፴', '፴፻፴');
  t.equal(geez('3033'), '፴፻፴፫', '፴፻፴፫');
